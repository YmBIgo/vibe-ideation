/***** 新素材の使い方を検索するPoCのプロンプト *****
 * 
 * プロンプト例
 * 
 * プロンプト1：製品から動作の流れを抽出（動作の特徴も抽出）
 * プロンプト2：製品の動作から、使われる素材一覧と、各素材において動作を担保する要件を抽出
 * プロンプト3：LLMアイデア生成。アイデア生成後に、要件を満たせるか、既存素材と比べて何が優れているかを文章化し、点数付けする
 * 
*/

export const PROMPT1 = `あなたはあらゆる家電製品の原理に詳しい専門家です。
入力された家電製品から、その原理を調べ、動作の流れを教えてください。

- 動作の流れは、動作の特徴とカテゴリを含めて、できるだけ細かく抽出してください。
カテゴリは、以下の例を参考にしてください。
例）以下のもの以外もあります。必要に応じて柔軟にカテゴリを追加してください。
  * 熱処理：加熱、冷却など、温度を変化させる動作
  * 物理的な変化：切断、粉砕、混合など、物理的な状態を変化させる動作
  * 湿度管理：蒸らし、乾燥など、湿度を管理する動作
  * 温度管理：保温、冷蔵など、温度を一定に保つ動作
- 抽出する「動作の流れ」は、最大８個までにしてください。
- 抽出した動作の流れと特徴は、JSON形式で出力してください。
JSON形式以外の出力は、エラーとして処理されますので、必ずJSON形式で出力してください。
また、JSONの形式は以下を参考にしてください。

\`\`\`JSONの形式
{
  "flow": [
    {
      "process": "過程1",
      "features": ["特徴1", "特徴2", "特徴3"],
      "category": "カテゴリ1"
    },
    ...(過程の数だけ繰り返し)
}
\`\`\`

例）
\`\`\`入力
炊飯器
\`\`\`

\`\`\`出力
{
  "flow": [
    {
      "process": "加熱",
      "feature": ["高温", "均一な加熱", "圧力の調整"],
      "category": "熱処理"
    },
    {
      "process": "蒸らし",
      "feature": ["適切な湿度", "一定の温度", "時間の管理"],
      "category": "湿度管理"
    },
    {
      "process": "保温",
      "feature": ["低温", "湿度の管理", "長時間の維持"],
      "category": "温度管理"
    }
  ]
}
\`\`\`
`;

