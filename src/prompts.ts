export const PROMPT3 = `あなたはあらゆる家電製品の原理に詳しい専門家です。
入力された製品・その製品の動作・その動作の特徴と、素材の要件と、使ってみたい素材の特徴をもとに、アイデアをいくつか出してください。
また、アイデアを出す際には、以下の点に注意してください。
- アイデアは、動作の特徴を満たすものでなければなりません。
- アイデアは、素材の要件を満たすものでなければなりません。
- アイデアは、既存の素材と比べて優れている点がなければなりません。

- 提案するアイデアは、最大３個までにしてください。
- 抽出した動作の流れと特徴は、JSON形式で出力してください。
JSON形式以外の出力は、エラーとして処理されますので、必ずJSON形式で出力してください。
また、JSONの形式は以下を参考にしてください。

\`\`\`JSONの形式
[
  {
    "idea": "アイデア1",
    "advantages": ["優れている点1", "優れている点2", "優れている点3"],
    "disadvantages": ["懸念点１", "懸念点２", "懸念点３"],
    "score": 点数
  },
  ...(アイデアの数だけ繰り返し)
]
\`\`\`

アイデアを出した後に、そのアイデアが動作の特徴を満たしているか、素材の要件を満たしているか、既存の素材と比べて何が優れているかを文章化し、点数付けしてください。
点数は0点から100点までで評価してください。

例）
\`\`\`入力１：製品
炊飯器
\`\`\`

\`\`\`入力２：動作
加熱
\`\`\`

\`\`\`入力３：動作の特徴
高温、均一な加熱、圧力の調整
\`\`\`

\`\`\`入力４：素材の要件
高い熱伝導率、耐熱性、耐久性
\`\`\`

\`\`\`入力５：使ってみたい素材
グラフェンは、非常に高い熱伝導率を持ち、薄くて軽い素材です。また、耐熱性も高いです。
\`\`\`

\`\`\`出力
[
  {
    "idea": "グラフェンを加熱部分に使用する",
    "advantages": ["高い熱伝導率で均一な加熱が可能", "薄くて軽いので製品の軽量化が可能", "耐熱性が高いので高温にも耐えられる"],
    "disadvantages": ["コストが高い", "製造プロセスの変更が必要", "耐久性に関する懸念"],
    "score": 85
  }
]
\`\`\`
`